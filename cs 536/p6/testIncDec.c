#include <stdio.h>
void main() {
  int x;
  x = 0;
  x++;
  printf("%d", x);
  printf("\n");
  x--;
  printf("%d", x);
  printf("\n");
  x = 10;
  ++x;
  printf("%d", x);
  printf("\n");
  --x;
  printf("%d", x);
  printf("\n");
  x = 10;
  printf("%d", x++);
  printf("\n");
  printf("%d", x);
  printf("\n");
  printf("%d", x--);
  printf("\n");
  printf("%d", x);
  printf("\n");
}
