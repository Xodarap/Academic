#include <stdio.h>
/* test all equality operators with int operands */

void main() {
  if (5 == 5) {
    printf("5 == 5 is TRUE (as expected)\n");
  } else {
    printf("PROBLEM: 5 == 5 is FALSE!\n");
  }

  if (5 != 5) {
    printf("PROBLEM: 5 != 0 is TRUE!\n");
  } else {
    printf("5 != 0 is FALSE (as expected)\n");
  }
}
